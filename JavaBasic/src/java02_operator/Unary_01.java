package java02_operator;

public class Unary_01 {
	public static void main(String[] args) {
		
		//단항연산자 - 부호, Sign
		// + -
		
		int num1 = -7; //음수
		int num2 = +7; //양수
		
		System.out.println( num1 );
		
		System.out.println( -num1 );
		
		//-----------------------------------
		
		// ! : 논리부정 연산자
		// (type) : 형변환 연산자
		
	}
}
